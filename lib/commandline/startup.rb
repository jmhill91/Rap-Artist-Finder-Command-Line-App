  def self.start
    system 'clear'
    puts "Wassup, welcome to Rapper Finder! Can't decide what Rapper you want to listen to. Please allow me to help you today."
    sleep (0.05)
    self.getting_started
  end

  def self.getting_started
    prompt = TTY::Prompt.new
    self.logo
    puts "Allow us to get stated by find out something about you"
    puts "\n"
    choices = ["User", "Rapper"]
    pic = prompt.select('Are you a "User" or a "Rapper"?' + "\n", choices)
    if pic == "User"
      UserInterface.lets_start
    else
    RapperInterface.artist_start
    end
  end

  def self.exit
    system 'clear'
    self.logo
    puts "Be Easy."
    sleep 3/2
    system 'exit!'
  end


